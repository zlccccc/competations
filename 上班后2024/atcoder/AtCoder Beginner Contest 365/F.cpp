template<class Info>
struct SegmentTree {
    int n;
    std::vector<Info> info;
    SegmentTree() : n(0) {}
    SegmentTree(int n_, Info v_ = Info()) {
        init(n_, v_);
    }
    template<class T>
    SegmentTree(std::vector<T> init_) {
        init(init_);
    }
    void init(int n_, Info v_ = Info()) {
        init(std::vector(n_, v_));
    }
    template<class T>
    void init(std::vector<T> init_) {
        n = init_.size();
        info.assign(4 << std::__lg(n), Info());
        std::function<void(int, int, int)> build = [&](int p, int l, int r) {
            if (r - l == 1) {
                info[p] = init_[l];
                return;
            }
            int m = (l + r) / 2;
            build(2 * p, l, m);
            build(2 * p + 1, m, r);
            pull(p);
        };
        build(1, 0, n);
    }
    void pull(int p) {
        info[p] = info[2 * p] + info[2 * p + 1];
    }
    void modify(int p, int l, int r, int x, const Info &v) {
        if (r - l == 1) {
            info[p] = v;
            return;
        }
        int m = (l + r) / 2;
        if (x < m) {
            modify(2 * p, l, m, x, v);
        } else {
            modify(2 * p + 1, m, r, x, v);
        }
        pull(p);
    }
    void modify(int p, const Info &v) {
        modify(1, 0, n, p, v);
    }
    Info rangeQuery(int p, int l, int r, int x, int y) {
        if (l >= y || r <= x) {
            return Info();
        }
        if (l >= x && r <= y) {
            return info[p];
        }
        int m = (l + r) / 2;
        auto ret = rangeQuery(2 * p, l, m, x, y) + rangeQuery(2 * p + 1, m, r, x, y);
        // printf("%d - %d\n",l,r-1);
        return ret;
    }
    Info rangeQuery(int l, int r) {
        return rangeQuery(1, 0, n, l, r);
    }
    template<class F>
    int findFirst(int p, int l, int r, int x, int y, F pred) {
        if (l >= y || r <= x || !pred(info[p])) {
            return -1;
        }
        if (r - l == 1) {
            return l;
        }
        int m = (l + r) / 2;
        int res = findFirst(2 * p, l, m, x, y, pred);
        if (res == -1) {
            res = findFirst(2 * p + 1, m, r, x, y, pred);
        }
        return res;
    }
    template<class F>
    int findFirst(int l, int r, F pred) {
        return findFirst(1, 0, n, l, r, pred);
    }
    template<class F>
    int findLast(int p, int l, int r, int x, int y, F pred) {
        if (l >= y || r <= x || !pred(info[p])) {
            return -1;
        }
        if (r - l == 1) {
            return l;
        }
        int m = (l + r) / 2;
        int res = findLast(2 * p + 1, m, r, x, y, pred);
        if (res == -1) {
            res = findLast(2 * p, l, m, x, y, pred);
        }
        return res;
    }
    template<class F>
    int findLast(int l, int r, F pred) {
        return findLast(1, 0, n, l, r, pred);
    }
};

struct Info {
    int l,r,outl,outr;
    ll value;
    Info(int l=0,int r=INF,int outl=0,int outr=INF,ll value=0):l(l),r(r),outl(outl),outr(outr),value(value) {};
};
Info operator+(const Info &c,const Info &b) {
    Info a=c;
    if (a.outl<b.l) {
        if (a.outr<b.l) {
            a.value+=b.l-a.outr;
            a.l=a.r=a.r;  // from r
            a.outl=a.outr=b.l;
        } else {
            a.outl=b.l;
            a.l=b.l;
        }
    }
    if (a.outr>b.r) {
        if (a.outl>b.r) {
            a.value+=a.outl-b.r;
            a.l=a.r=a.l;  // from r
            a.outl=a.outr=b.r;
        } else {
            a.outr=b.r;
            a.r=b.r;
        }
    }
    if (b.outl==b.outr) a.outl=a.outr=b.outl;  // single
    a.value+=b.value;
    // printf("mid-solve a = %d %d %d %d %lld\n",c.l,c.r,c.outl,c.outr,c.value);
    // printf("mid-solve b = %d %d %d %d %lld\n",b.l,b.r,b.outl,b.outr,b.value);
    // printf("mid-solve fin %d %d %d %d %lld\n",a.l,a.r,a.outl,a.outr,a.value);
    return a;
}

