int n;
void getres(int x,int fa) {
    // cut all x-to-sub-edges
    int cur=res;
    if (sub[x]&&edge[x].size()==1) cur++;
    ans=max(ans,cur);
    // printf("%d: cur=%d\n",x,cur);
    // FOR_(i,1,n) printf("%d ",sub[i]); puts("<- sub");
    for (int v:edge[x]) {
        // cut v-to-sub
        if (v==fa) continue;
        if (!sub[x]) res--;
        if (!sub[v]) res--;
        if (!sub[v]) sub[x]--;
        if (!sub[x]) sub[v]++;  // x->v
        if (!sub[x]) res++;
        if (!sub[v]) res++;
        getres(v,x);
        if (!sub[x]) res--;
        if (!sub[v]) res--;
        if (!sub[x]) sub[v]--;  // x->v
        if (!sub[v]) sub[x]++;
        if (!sub[x]) res++;
        if (!sub[v]) res++;
    }
}
