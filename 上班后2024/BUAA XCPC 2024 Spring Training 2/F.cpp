    ar4 fin={0,0,0,0};
    for (auto &[val,vec]:MP) {
        ar4 cur={0,0,0,val};  // {cnt,l,r,value};
        for (int v:vec) {
            cur[0]-=v-cur[2]-1; cur[0]++;
            if (cur[0]<=1) cur={1,v,v,val};
            else cur[2]=v;
            fin=max(fin,cur);
        }
    }
    printf("%d %d %d\n",fin[3],fin[1],fin[2]);
