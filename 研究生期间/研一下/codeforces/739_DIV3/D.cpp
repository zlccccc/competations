int A[maxn],B[maxn];
int solve(ll a,ll b){
    // a -> b
    int number=0,tot1=0,tot2=0;
    while (a) A[++tot1]=a%10,a/=10,number++;//remove
    while (b) B[++tot2]=b%10,b/=10,number++;//insert
    while (tot2) {
        while (tot1&&A[tot1]!=B[tot2]) tot1--;
        if (tot1) tot1--,tot2--,number-=2;//reuse
        else break;
    }
    // printf("%lld -> %lld: number=%d\n",a,b,number);
    return number;
}
