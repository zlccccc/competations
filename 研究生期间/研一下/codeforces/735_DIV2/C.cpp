ll solve(ll n,ll m,int k) {
    // 0...x
    // printf("solve %lld %lld %d\n",n,m,k);
    if (m==(1ll<<(k+1))-1) return 1ll<<(k+1);
    if (n!=0&&m==0) return 0;
    if (n==0&&m==0) return 1;
    if ((n>>k)&1) {
        if (m<(1ll<<k)) return 0;//no value
        return solve(n^(1ll<<k),m^(1ll<<k),k-1);
    } else {
        if (m<(1ll<<k)) return solve(n,m,k-1);
        return solve(n,m^(1ll<<k),k-1)+(1<<k);
    }
}
