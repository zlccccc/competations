vector<int> edge[maxn];
int sz[maxn],rm[maxn];
void dfs(int x,int fa) {
    int cnt=0; sz[x]=1; rm[x]=0;
    int MAX=-INF;
    for (int v:edge[x]) {
        if (v==fa) continue;
        dfs(v,x);
        cnt++; sz[x]+=sz[v];
        max_(MAX,rm[v]-sz[v]+1);
    }
    if (cnt==1) rm[x]=sz[x]-2;
    else if (cnt==2) rm[x]=sz[x]-3+MAX;
    else rm[x]=0;
    // printf("%d: %d %d\n",x,sz[x],rm[x]);
}
